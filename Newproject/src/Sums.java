
public class Sums {

	public void findAdd(int num1,int num2 ) {
			
		System.out.println(num1+num2);
	}
	public void findSubtraction(int num1,int num2) {
		System.out.println(num1-num2);
	}
	public void findMultiply(int num1,int num2) {
		System.out.println(num1*num2);
	}
	public void findDivied(int num1,int num2) {
		System.out.println(num1/num2);
	}
	
}
